function add(x,y){
    return x+ y;
};

function subtract(x,y){
    return x- y;
};

function multiple(x,y){
    return x* y;
};

function divide(x,y){
    return x/y;
};

const pi = 3.14;

 module.exports ={
    add: add,
    subtract: subtract,
    multiply: multiple,
    divide: divide,
    pi:pi
 }
